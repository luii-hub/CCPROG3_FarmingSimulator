package base.myFarm;

/**
 * This enum represents the type of Crop that a Crop 'Object' has
 */
public enum CropType {
    /**
     * A Crop with this type is considered a Root Crop.
     */
    ROOT_CROP,
    /**
     * A Crop with this type is considered a Flower Crop.
     */
    FLOWER,
    /**
     * A Crop with this type is considered a Fruit Tree Crop.
     */
    FRUIT_TREE,
}